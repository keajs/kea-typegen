func renderTypedFormInternalExtraInput(logic ParsedLogic) string {
	formType := logic.ExtraInputForm
	defaultType := "Record<string, any>"
	lines := []string{
		"{",
		"    form:",
		"        | {",
		fmt.Sprintf("              default?: %s", defaultType),
		fmt.Sprintf("              submit?: (form: %s) => void", formType),
		"          }",
		fmt.Sprintf("        | ((logic: %s) => {", logic.TypeName),
		fmt.Sprintf("              default?: %s", defaultType),
		fmt.Sprintf("              submit?: (form: %s) => void", formType),
		"          })",
		"}",
	}
	return strings.Join(lines, "\n")
}

func renderPathTuple(path []string) string {
	if len(path) == 0 {
		return "[]"
	}
	parts := make([]string, 0, len(path))
	for _, item := range path {
		parts = append(parts, quoteString(item))
	}
	return "[" + strings.Join(parts, ", ") + "]"
}

func writeProperty(builder *strings.Builder, indent, name, typeText string) {
	lines := strings.Split(strings.TrimSpace(typeText), "\n")
	if len(lines) == 1 {
		builder.WriteString(fmt.Sprintf("%s%s: %s\n", indent, name, lines[0]))
		return
	}

	builder.WriteString(fmt.Sprintf("%s%s: %s\n", indent, name, lines[0]))
	for _, line := range lines[1:] {
		builder.WriteString(indent)
		builder.WriteString(line)
		builder.WriteByte('\n')
	}
}

func actionTypeString(logic ParsedLogic, actionName string) string {
	return camelCaseToWords(actionName) + " (" + logic.PathString + ")"
}

func camelCaseToWords(text string) string {
	var builder strings.Builder
	for index, r := range text {
		if r >= 'A' && r <= 'Z' {
			if index > 0 {
				builder.WriteByte(' ')
			}
			builder.WriteRune(r + ('a' - 'A'))
			continue
		}
		builder.WriteRune(r)
	}
	return builder.String()
}

func quoteString(text string) string {
	return "'" + strings.ReplaceAll(text, "'", "\\'") + "'"
}

func renderPropertyName(name string) string {
	if isSimpleIdentifier(name) {
		return name
	}
	return quoteString(name)
}

func isSimpleIdentifier(text string) bool {
	text = strings.TrimSpace(text)
	if text == "" {
		return false
	}
	for i := 0; i < len(text); i++ {
		ch := text[i]
		if i == 0 {
			if !isIdentifierStart(ch) {
				return false
			}
			continue
		}
		if !isIdentifierPart(ch) {
			return false
		}
	}
	return true
}

func fallbackType(value, fallback string) string {
	if strings.TrimSpace(value) == "" {
		return fallback
	}
	return value
}

func boolLiteral(value bool) string {
	if value {
		return "true"
	}
	return "false"
}
