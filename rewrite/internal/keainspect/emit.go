func renderReducer(logic ParsedLogic) string {
	lines := []string{
		"(",
		"    state: any,",
		"    action: any,",
		"    fullState: any,",
		fmt.Sprintf(") => %s", renderFieldShape(logic.Reducers)),
	}
	return strings.Join(lines, "\n")
}

